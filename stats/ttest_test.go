// Edge case PairedTTest cannot run via R (R rejects all-zero diffs).
// Test that insyra returns NaN/Inf as appropriate for degenerate inputs.
func TestPairedTTest_AllZeroDiff(t *testing.T) {
	a := insyra.NewDataList([]float64{5, 5, 5, 5})
	b := insyra.NewDataList([]float64{5, 5, 5, 5})
	r, err := stats.PairedTTest(a, b, 0.95)
	if err != nil {
		t.Fatalf("PairedTTest error: %v", err)
	}
	// stddev=0, meanDiff=0 → tValue = 0/0 = NaN; effect size = 0/0 = NaN.
	if !math.IsNaN(r.Statistic) {
		t.Errorf("expected NaN t, got %v", r.Statistic)
	}
	if r.MeanDiff == nil || *r.MeanDiff != 0 {
		t.Errorf("expected meanDiff=0, got %v", r.MeanDiff)
	}
	if r.N != 4 {
		t.Errorf("n: got %d, want 4", r.N)
	}
}
