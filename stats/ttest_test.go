func TestSingleSampleTTest_ConstantData(t *testing.T) {
	// Special cases R cannot test because t.test() errors on essentially constant data.
	// Tested against insyra's documented edge-case behavior (see ttest.go SingleSampleTTest).

	t.Run("data_equals_mu_constant", func(t *testing.T) {
		dl := insyra.NewDataList([]float64{50, 50, 50, 50, 50})
		r, err := stats.SingleSampleTTest(dl, 50, 0.95)
		if err != nil {
			t.Fatalf("error: %v", err)
		}
		if !math.IsNaN(r.Statistic) {
			t.Errorf("expected NaN t, got %v", r.Statistic)
		}
		if !math.IsNaN(r.PValue) {
			t.Errorf("expected NaN p, got %v", r.PValue)
		}
		if r.DF == nil || *r.DF != 4 {
			t.Errorf("df: got %v, want 4", r.DF)
		}
		if r.CI[0] != 50 || r.CI[1] != 50 {
			t.Errorf("CI: got %v, want [50,50]", r.CI)
		}
		if r.Mean == nil || *r.Mean != 50 {
			t.Errorf("mean: got %v, want 50", r.Mean)
		}
		if len(r.EffectSizes) != 1 || r.EffectSizes[0].Value != 0 {
			t.Errorf("cohen_d: got %+v, want 0", r.EffectSizes)
		}
	})

	t.Run("constant_data_not_equal_mu", func(t *testing.T) {
		dl := insyra.NewDataList([]float64{10, 10, 10})
		r, err := stats.SingleSampleTTest(dl, 5, 0.95)
		if err != nil {
			t.Fatalf("error: %v", err)
		}
		if !math.IsInf(r.Statistic, 1) {
			t.Errorf("expected +Inf t, got %v", r.Statistic)
		}
		if r.PValue != 0 {
			t.Errorf("expected p=0, got %v", r.PValue)
		}
		if r.DF == nil || *r.DF != 2 {
			t.Errorf("df: got %v, want 2", r.DF)
		}
		if r.CI[0] != 10 || r.CI[1] != 10 {
			t.Errorf("CI: got %v, want [10,10]", r.CI)
		}
		if len(r.EffectSizes) != 1 || !math.IsInf(r.EffectSizes[0].Value, 1) {
			t.Errorf("cohen_d: expected +Inf, got %+v", r.EffectSizes)
		}
	})
}

