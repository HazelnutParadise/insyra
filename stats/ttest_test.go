// loadF64s reads a labelled comma-separated array out of
// testdata/ttest_data_dump.txt — produced by ttest_data_dump.R so that test
// inputs and the R reference values come from byte-identical data.
var (
	loadF64sOnce  sync.Once
	loadF64sCache map[string][]float64
)

func loadF64s(t *testing.T, label string) []float64 {
	t.Helper()
	loadF64sOnce.Do(func() {
		loadF64sCache = map[string][]float64{}
		raw, err := os.ReadFile("testdata/ttest_data_dump.txt")
		if err != nil {
			t.Fatalf("read testdata/ttest_data_dump.txt: %v", err)
		}
		for _, line := range strings.Split(string(raw), "\n") {
			line = strings.TrimSpace(line)
			if line == "" {
				continue
			}
			i := strings.IndexByte(line, ':')
			if i < 0 {
				continue
			}
			name := line[:i]
			parts := strings.Split(line[i+1:], ",")
			vals := make([]float64, 0, len(parts))
			for _, p := range parts {
				p = strings.TrimSpace(p)
				if p == "" {
					continue
				}
				v, err := strconv.ParseFloat(p, 64)
				if err != nil {
					t.Fatalf("parse %s value %q: %v", name, p, err)
				}
				vals = append(vals, v)
			}
			loadF64sCache[name] = vals
		}
	})
	v, ok := loadF64sCache[label]
	if !ok {
		t.Fatalf("loadF64s: label %q not found in testdata/ttest_data_dump.txt", label)
	}
	return v
}

// Reference values generated by stats/testdata/ttest_reference.R using R 4.5.1's
// t.test() (the mainstream library for Student's t-tests). Cohen's d is computed
// in R using the same formula insyra uses (see ttest_reference.R header).
//
// Tolerances (chosen empirically — distuv vs R's pt/qt implementations of the
// Student's t CDF/quantile produce results that agree to roughly 1e-12 .. 1e-14
// in this range; CI bounds and p-values inherit that error):
//   tolStat = 1e-12   (t statistic, mean, mean diff, Cohen's d)
//   tolP    = 1e-12   (p-value)
//   tolCI   = 1e-10   (CI bounds, multiplied by t-quantile)
//   df is exact match for integer-valued, 1e-12 for Welch.
const (
	tolStat = 1e-12
	tolP    = 1e-12
	tolCI   = 1e-10
)

func tEqualOrNaNInf(a, b, tol float64) bool {
	if math.IsNaN(a) && math.IsNaN(b) {
		return true
	}
	if math.IsInf(a, 0) && math.IsInf(b, 0) && math.Signbit(a) == math.Signbit(b) {
		return true
	}
	if math.IsNaN(a) != math.IsNaN(b) || math.IsInf(a, 0) != math.IsInf(b, 0) {
		return false
	}
	if b == 0 {
		return math.Abs(a) <= tol
	}
	return math.Abs(a-b) <= tol*math.Max(1, math.Abs(b))
}

